/*
 * tools/build-coastlines.js — regenerate ../coastlines.js from Natural Earth.
 *
 * Usage:  node tools/build-coastlines.js
 *
 * Downloads the public-domain Natural Earth 1:50m coastline
 * (github.com/nvkelso/natural-earth-vector), clips it to the Atlantic basin
 * box (lon -110..5, lat -10..43 — covers the map's maxBounds with margin),
 * rounds coordinates to 2 decimals, and rewrites coastlines.js as a single
 * MultiLineString feature. Dev-time only; zero dependencies; never runs in
 * the browser.
 */
'use strict';
const fs = require('fs');
const path = require('path');
const https = require('https');

const URL =
  'https://raw.githubusercontent.com/nvkelso/natural-earth-vector/master/geojson/ne_50m_coastline.geojson';
const BOX = { w: -110, e: 5, s: -10, n: 43 };
const OUT = path.join(__dirname, '..', 'coastlines.js');

const inBox = (p) => p[0] >= BOX.w && p[0] <= BOX.e && p[1] >= BOX.s && p[1] <= BOX.n;
const r2 = (v) => Math.round(v * 100) / 100;

// Keep runs of in-box points; retain one out-of-box point at each entry/exit
// so lines meet the frame edge instead of stopping short of it.
function clipLines(geojson) {
  const out = [];
  for (const f of geojson.features) {
    const lines = f.geometry.type === 'LineString' ? [f.geometry.coordinates]
      : f.geometry.type === 'MultiLineString' ? f.geometry.coordinates : [];
    for (const line of lines) {
      let run = [];
      for (let i = 0; i < line.length; i++) {
        const p = line[i];
        if (inBox(p)) {
          if (!run.length && i > 0) run.push([r2(line[i - 1][0]), r2(line[i - 1][1])]);
          run.push([r2(p[0]), r2(p[1])]);
        } else if (run.length) {
          run.push([r2(p[0]), r2(p[1])]);
          if (run.length >= 2) out.push(run);
          run = [];
        }
      }
      if (run.length >= 2) out.push(run);
    }
  }
  return out;
}

function fetchJson(url) {
  return new Promise((resolve, reject) => {
    https.get(url, (res) => {
      if (res.statusCode !== 200) return reject(new Error('HTTP ' + res.statusCode));
      let body = '';
      res.on('data', (c) => (body += c));
      res.on('end', () => resolve(JSON.parse(body)));
    }).on('error', reject);
  });
}

fetchJson(URL).then((geojson) => {
  const lines = clipLines(geojson);
  const js = JSON.stringify(lines);
  const file = `/*
 * coastlines.js — Hurricane Console
 * GENERATED by tools/build-coastlines.js — do not hand-edit.
 * Natural Earth 1:50m coastline (public domain), clipped to lon -110..5 /
 * lat -10..43, rounded to 0.01 deg. ${lines.length} polylines, ~${Math.round(js.length / 1024)} KB.
 * This embedded layer is the guaranteed offline basemap; the CARTO tile layer
 * in app.js is a progressive enhancement on top of it.
 */
(function (root) {
  'use strict';
  root.BASIN_COASTLINES = {
    type: 'FeatureCollection',
    features: [
      { type: 'Feature', properties: { name: 'ne50-coastline' },
        geometry: { type: 'MultiLineString', coordinates:
${js}
      } }
    ]
  };
  if (typeof module !== 'undefined' && module.exports) module.exports = root.BASIN_COASTLINES;
})(typeof window !== 'undefined' ? window : globalThis);
`;
  fs.writeFileSync(OUT, file);
  console.log('wrote', OUT, '—', lines.length, 'lines,', Math.round(file.length / 1024), 'KB');
}).catch((e) => {
  console.error('build failed:', e.message);
  process.exit(1);
});
